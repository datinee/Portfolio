  <h1> Qui Sommes-nous ?  </h1>
 <p class = "text-justify  h3"> 
 
Un groupe d’amis résidents en France et au Maghreb, conscients que nos pays ont besoin de toutes les bonnes volontés pour participer à leur construction. 
Le domaine éducatif, et plus particulièrement en destination des plus jeunes, a été ciblé. Nous avons constitué une association loi 1901 sous le nom de l’ECEFRAM, Echanges et Coopération Educatifs France-Maghreb. L’association a été agréée le 17 juin 2003 sous le N° 094201743.
 
